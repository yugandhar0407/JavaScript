//Arithmetic Operators
// +,-,*,/,%,++,--,**

//Addition [+]
let a = 10;
let b = a + 20;  //30
let c = a + b + 40; //80
let d = a + b + c; //120

console.log(d);

//Subtraction [-]
let sub = 200 - 400; //200
let sub1 = sub - 200; //-400

console.log(sub1);

//Multiplication [*]
var mul = 20 * 40;  //800
var mul1 = mul * 20; // 800*20 = 16000

console.log(mul1*mul);  // 16000*800 = 12800000

//Division [/]
const div = 120/5;  // 24
const div2 = div / 5; // 24/5 = 4.8
var div3 = div/div2; // 24/4.8 = 5

console.log(div3);

//Modulus
let mod = 145 % 10; //5
console.log(mod);

// Increment

 let z = 20;  //20
 z++;         // 20 + 1 = 21
 let y = z + 30; //21 + 31 = 52
 y++;           //31
 
 console.log(y);

// Decrement

let p = 40; //40
p--;        // 40 - 1 = 39;
let q = 80; //80
q--;        // 80 -1  = 79

console.log(q);


//Exponentiation   [power]

let e = 5;
let f =  e ** 2; //5 ** 2 = 5*5 = 25
console.log(f);


let g = 7;
let h = 4 ** g; // 4 ** 7 = 7*7*7*7 = 16384
console.log(h);


